fn main() {
    let n = 55;

    /* Creating an if-else statement */
    if n < 30
    {
        println!("Value is less than 30\n");
    }
    else if n == 45
    {
        println!("Value is equal to 45\n");
    }
    else
    {
        println!("Value is greater than 30 and not equal to 45\n");
    }
}
