fn main() {
    let x = 45; //integer-32
    let y: i64 = 32; //integer-64
    let z: u64 = 100; //unsigned-64
    let f      = 3.14; //float-32
    let p: f64 = 6.28; //float-64    
    let b: bool= false; //boolean defined
    let a      = true; //boolean undefined

    println!("The value of x is {}\n",x);
    println!("The value of y is {}\n",y);
    println!("The value of z is {}\n",z);
    println!("The value of f is {}\n",f);
    println!("The value of p is {}\n",p);
    println!("The value of b is {}\n",b);
    println!("The value of a is {}\n",a);
}
